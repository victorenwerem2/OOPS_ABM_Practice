public class Addition {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int sum = a + b;
        System.out.println("Sum = " + sum);
    }
}

public class Subtraction {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int result = a - b;
        System.out.println("Result = " + result);
    }
}